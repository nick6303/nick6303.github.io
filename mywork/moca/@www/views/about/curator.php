        <p>From The Director 館長的一席話</p>
        <ul>
          <li data-watermark="01">
            <p class="h4">BACK TO THE ORIGINALITY OF ART</p>
            <p class="h5">回到藝術的原初性</p>
            <p>台北當代藝術館聚焦在「當代性」方面比臺灣其他各大美術館的任務輕快而慧頡，有帶領風騷的鮮明性格。基於這個定位，我們不斷往前求新求變的同時，也要不斷回到藝術的原初性，這就是「走在世界的前端 --- Walking in the front end of the world」，同時也要「禮失而求諸野 --- If the proper knowledge of rituals is lost, then look for it in the wilds」。
              位於文化風潮中心的臺北都會，潮流變化很快，藝術難免成為符應前衛思潮的產物，與存在無關，於是我們必須覺察文化疲敝的時刻，如何找回野性，恢復生命力。</p>
          </li>
