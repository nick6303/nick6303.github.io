            <p class="h4">CHANGE, TO ADJUST TO THE CITY'S STRONG CREATIVITY</p>
            <p class="h5">改變，因應城市越來越強旺的創造力</p>
            <p>台北當代藝術館聚焦在「當代性」方面比臺灣其他各大美術館的任務輕快而慧頡，有帶領風騷的鮮明性格。基於這個定位，我們不斷往前求新求變的同時，也要不斷回到藝術的原初性，這就是「走在世界的前端 --- Walking in the front end of the world」，同時也要「禮失而求諸野 --- If the proper knowledge of rituals is lost, then look for it in the wilds」。
              位於文化風潮中心的臺北都會，潮流變化很快，藝術難免成為符應前衛思潮的產物，與存在無關，於是我們必須覺察文化疲敝的時刻，如何找回野性，恢復生命力。</p>
          </li>
          <li data-watermark="03" class="close">
            <p class="h4">EXHIBITION HAVE NEW MEDIUM & NEW MODUS</p>
            <p class="h5">當代的展覽，它的新媒材與新形式</p>
            <p>藝術品與藝術欣賞不是少數特殊階級的專利，而是全體市民的人文資產，所有的人可以按自己的品味自由接觸，這是當代館的社會責任。
                當代館近年參觀人數不斷上升，以年輕世代的觀眾居多，相關研究顯示，由於地緣關係老城區的文化生活是原因之一，最重要的還是展覽的「當代性」，它的新媒材與新形式、既貼近生活又超越現實、新奇而又強大的視覺震撼，當代藝術的魅力無窮。然而，我們深知網路趕潮與藝術愛好之間，差之毫釐失之千里，不可等同視之。除此之外，對於無法接受當代藝術的前衛與爭議者，需要更好的方法策略，基於前述兩個問題，我們的責任在於努力揭示藝術中的人文精神，無論作品如何前衛與極端，爭議與批判，流行與媚俗，都要闡釋它的人文內涵，讓市民透過當代藝術而擴大人類的視野、了解世界，市民對當代藝術的欣賞與熱情，即是對人性最深沉的體會與最大寬容。</p>
          </li> 
          <li data-watermark="04">
            <p class="h4">ART EXHIBITIONS CLOSE TO THE LIVING SPACES</p>
            <p class="h5">將藝術展演帶到都會及社區空間</p>
            <p>除了人文精神的闡述與擴充之外，我們持續過去的成就，將藝術展演帶到都會及社區空間，與臺灣人美好的人情結合。從鄰近社區的事件、故事、歷史為題材，透過年輕藝術家用藝術的各種手法表現與生活結合，讓城市更有魅力。</p>
          </li>         
        </ul>
