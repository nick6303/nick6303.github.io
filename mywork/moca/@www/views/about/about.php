        <p class="h5">FROM ELEMENTARY SCHOOL TO MOCA</p>
        <p class="h5">從日治小學校到當代藝術館</p>
        <p>台北當代藝術館的館舍建築，落成於西元1921年，原為日治時期專供日本人子弟受教育的「建成小學校」。1945年後，台灣進入中華民國時代，原建成小學校舍被轉用為台北市政府辦公廳舍，直至1994年台北市政府遷入信義區的新市政中心為止。在這五十年當中，這棟紅磚建築體，正是驅動台北市政及各類軟硬建設的神經中樞。<br><br>1996年，台北市政府舊廈被登錄為市定古蹟，並基於古蹟再利用的政策，將原建物之正面廳舍整建為「台北當代藝術館」專用，兩翼部分建築則劃歸為建成國中教室。「台北當代藝術館」與「建成國中」新校的結合，不僅創造了全世界罕見美術館與學校使用共同建物的先例，也被視為是台北市孔廟到保安宮這一條歷史文化軸線的延展，而當代藝術館也象徵了帶動大同區再發展的一個新契機。在國內藝術界殷殷關注之下，市定古蹟的台北舊市府，活化轉用成為國內唯一的「當代藝術館」，並於2001年5月26日正式開幕，從此成為台北文化地圖上的一個新地標。</p>  
