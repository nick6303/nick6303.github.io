        <p class="h5">多元文化與跨界創意</p>
        <p>台北當代藝術館自2001年開館後，前七年由市政府委託民間企業家組成的「當代藝術基金會」統籌營運，基金會董事和歷任館長、館員們同心合力，陸續辦理了許多精采絕倫的當代藝術展覽，除了致力推動台灣與國際藝壇的對話，也用心開拓民眾的美感品味和文化視野。2008年1月1日起，為期七年的「公辦民營」合約結束，台北當代藝術館轉由台北市文化基金會接手營運。新的經營團隊除了延續之前的營運理念和方向，也進一步匯聚海內外的關注，結合官方有限資源與民間贊助力量，媒合多元文化和跨界創意。除了扮演國際當代藝術展覽的重要據點，和彰顯台灣當代藝術成果的焦點櫥窗，當代館始終自我期許的三個目標：一是推動多元風貌的藝術創作與展覽，二是著眼於激發民眾的新觀點和新思維，三是提供當代城市發展源源不絕的創意與活力。</p>  
